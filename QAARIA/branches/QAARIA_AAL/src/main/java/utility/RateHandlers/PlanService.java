package utility.RateHandlers;

import java.util.ArrayList;

/**
 * Created by joe on 3/10/16.
 */
public class PlanService
{
    private String usageTypeCode;
    private String clientServiceId;
    private String usageUnitLabel;
    private ArrayList<ServiceRate> serviceRates;
    public PlanService()
    {

    }

    public String getUsageTypeCode()
    {
        return usageTypeCode;
    }

    public void setUsageTypeCode(String usageTypeCode)
    {
        this.usageTypeCode = usageTypeCode;
    }

    public String getClientServiceId()
    {
        return clientServiceId;
    }

    public void setClientServiceId(String clientServiceId)
    {
        this.clientServiceId = clientServiceId;
    }

    public ArrayList<ServiceRate> getServiceRates()
    {
        return serviceRates;
    }

    public void setServiceRates(ArrayList<ServiceRate> serviceRates)
    {
        this.serviceRates = serviceRates;
    }

    public String getUsageUnitLabel()
    {
        return usageUnitLabel;
    }

    public void setUsageUnitLabel(String usageUnitLabel)
    {
        this.usageUnitLabel = usageUnitLabel;
    }
}
